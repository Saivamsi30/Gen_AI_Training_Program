# 1. Pydantic Output Schemas
# ============================================================

class ClassificationResult(BaseModel):
    category: str = Field(description="Primary category of the ticket extracted from the description")


class SeverityResult(BaseModel):
    priority: str = Field(description="Priority level: P1, P2, P3, or P4")
    impact_score: float = Field(description="Impact score between 0.0 and 1.0")
    reasoning: str = Field(description="Short reasoning for the severity assessment")


class ResolutionResult(BaseModel):
    summary: str = Field(description="Brief summary of the issue")
    root_cause_hypothesis: str = Field(description="Most likely root cause")
    resolution_steps: List[str] = Field(description="Ordered list of steps to resolve the issue")
    confidence_score: float = Field(description="Confidence score between 0.0 and 1.0 generated by LLM based on RAG context")


class ApprovalResult(BaseModel):
    approved: bool = Field(description="Whether the resolution plan is approved")
    risk_level: str = Field(description="Risk level: Low, Medium, or High")
    concerns: List[str] = Field(description="Any concerns about the proposed resolution")
    modifications: List[str] = Field(description="Suggested modifications or changes required")
    approval_notes: str = Field(description="Reasoning or notes from the approval process")


class TavilyRestructureResult(BaseModel):
    resolution_steps: List[str] = Field(description="Restructured resolution steps based on web search")
    confidence_score: float = Field(description="Re-scored confidence score between 0.0 and 1.0")


