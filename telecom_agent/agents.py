@tool
def check_outage_logs(created_at: str, region: str) -> str:
    """
    Checks the synthetic outage logs JSON file for any active outages in the specified region and date/time.
    Takes 'created_at' and 'region' and queries data/synthetic/outage_logs.json.
    """
    try:
        from config import DATA_DIR
        outage_file = os.path.join(DATA_DIR, "synthetic", "network-outage-incident-logs.json")
        if not os.path.exists(outage_file):
            return "Outage log file not found."
            
        with open(outage_file, "r", encoding="utf-8") as f:
            logs = json.load(f)
            
        # Parse ticket date - handle both formats:
        # Standard format: 'YYYY-MM-DD HH:MM:SS'
        # ISO format: 'YYYY-MM-DDTHH:MM:SSZ' or 'YYYY-MM-DDTHH:MM:SS'
        ticket_date_str = None
        try:
            # Try standard format first
            ticket_dt = datetime.strptime(created_at, "%Y-%m-%d %H:%M:%S")
            ticket_date_str = ticket_dt.strftime("%Y-%m-%d")
        except Exception:
            try:
                # Try ISO format with Z timezone
                ticket_dt = datetime.strptime(created_at.replace("Z", ""), "%Y-%m-%dT%H:%M:%S")
                ticket_date_str = ticket_dt.strftime("%Y-%m-%d")
            except Exception:
                # Fallback: extract date prefix from either format
                if "T" in created_at:
                    ticket_date_str = created_at.split("T")[0]
                else:
                    ticket_date_str = created_at.split(" ")[0]
            
        matched_outages = []
        for log in logs:
            log_region = log.get("affected_region", "")
            # Check if regions match (case-insensitive substring match)
            if region.lower() in log_region.lower() or log_region.lower() in region.lower():
                # Check if dates match - extract date from log (handles both formats)
                log_start = log.get("incident_start_time", "")
                # Handle both 'YYYY-MM-DD HH:MM:SS' and 'YYYY-MM-DDTHH:MM:SSZ' formats
                if "T" in log_start:
                    log_date_str = log_start.split("T")[0]
                else:
                    log_date_str = log_start.split(" ")[0]
                
                if ticket_date_str == log_date_str:
                    matched_outages.append({
                        "duration_minutes": log.get("duration_minutes", 0),
                        "affected_subs": log.get("customers_affected", 0),
                        "root_cause": log.get("root_cause", "Unknown Cause")
                    })
                    
        if matched_outages:
            return json.dumps(matched_outages[0], indent=2)
        else:
            return "No active outages found in region on that date."
            
    except Exception as e:
        return f"Outage check failed with error: {str(e)}"


@tool
def retrieve_customer_kb_context(query: str, category: str) -> str:
    """
    Tool wrapper for retrieving customer KB context from ChromaDB.
    Returns a formatted heading + body string or an explanatory message.
    """
    try:
        import chromadb
        from sentence_transformers import SentenceTransformer
        from config import VECTOR_STORE_DIR, SENTENCE_TRANSFORMER_MODEL

        model = SentenceTransformer(SENTENCE_TRANSFORMER_MODEL)
        client = chromadb.PersistentClient(path=VECTOR_STORE_DIR)
        collection = client.get_or_create_collection(name="customer_support_guide")

        allowed_metadata = ["Network_connectivity", "Service_outage", "Hardware_equipment", "Customer_experience"]
        pdf_title_val = category if category in allowed_metadata else "Service_outage"

        query_vector = model.encode(query).tolist()

        results = collection.query(
            query_embeddings=[query_vector],
            n_results=1,
            where={"category": pdf_title_val}
        )

        if results and results.get("documents") and len(results["documents"]) > 0 and len(results["documents"][0]) > 0:
            doc = results["documents"][0][0]
            metadata = results.get("metadatas", [[{}]])[0][0] if results.get("metadatas") else {}
            heading = metadata.get("heading", "Relevant Section")

            doc_lines = doc.splitlines()
            if doc_lines and doc_lines[0].strip() == heading:
                body = "\n".join(doc_lines[1:]).strip()
            else:
                body = doc

            return f"[Source: customer_support_guide > Category: {pdf_title_val} > Heading: {heading}]\n\n{body}"

        return "No relevant troubleshooting context found in the knowledge base."

    except BaseException as e:
        return f"RAG context retrieval failed: {str(e)}"


