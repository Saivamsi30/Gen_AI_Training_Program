class RestructuringAgent:
    """Restructures and rewrites final suggestion based on modifications provided during approval."""

    def __init__(self, api_key: str):
        self.llm = ChatGoogleGenerativeAI(
            model=MODEL_NAME,
            google_api_key=api_key,
            temperature=0
        )

    def restructure(self, original_steps: List[str], modifications: List[str], config: Optional[dict] = None) -> List[str]:
        original_text = "\n".join([f"- {s}" for s in original_steps])
        modifications_text = "\n".join([f"- {m}" for m in modifications])
        
        prompt = f"""You are a telecom incident supervisor. A senior manager approved a resolution plan but requested specific modifications.
        
        Original Resolution Plan Steps:
        {original_text}
        
        Requested Modifications:
        {modifications_text}
        
        Please rewrite and output the final resolution plan steps incorporating all modifications seamlessly.
        """
        
        class FinalSteps(BaseModel):
            steps: List[str] = Field(description="Final restructured list of steps")
            
        structured_llm = self.llm.with_structured_output(FinalSteps)
        result = structured_llm.invoke(prompt, config=config)
        return result.steps
